/**
 * Print the word doll in three different languages:
 * English (doll), Spanish (muñeca), and French (poupée). 
 *
 * @author Nachiketh Mamidi
 * @version Feb 4th, 2021
 */
public class DollPrinter
{
    public static void main(String[] args)
    {
        System.out.println("|******|");
        // In English
        System.out.println("|doll**|");
        // In Spanish 
        System.out.println("|mu\u00f1eca|");
        // In French
        System.out.println("|poup\u00e9e|");
        System.out.println("|******|");
    }
}